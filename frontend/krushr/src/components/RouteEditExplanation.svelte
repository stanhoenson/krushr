<h1>Creating a route</h1>
<p class="big">
  To create a route you need to fill out the form(s) below. Some fields have a
  black button to add a new entity of said field. When you press this button, a
  new empty field will be displayed.
</p>
<hr class="soft" />
<section class="grid explanation">
  <div>
    <h2>Adding points of interest</h2>
    <p>
      You can't have a route without points of interest, and this form looks
      very similar to the route form. The biggest difference are the latitude
      and longitude fields (and the big map). You can type these out manually or
      click a spot on the map.
    </p>
    <p>
      Just like with the other fields, press the black button beneath the point
      of interest form, to add a new one.
    </p>
  </div>
  <div>
    <h2>Saving or publishing the route</h2>
    <p>
      Press the blue save button on the bottom to save your progress. A route is
      unpublished by default, so don't worry to save a route you haven't
      finished.
    </p>
    <p>
      If the route is finished, change the status from unpublished to published
      and press the save button.
    </p>
  </div>
</section>
